#\n: Is to jump a line
cads='This is a \n single quote text'
#\t: Is to make a tabulation
cadd="This is a \n\t Double quote text"
#\': Is to add the single quote
cad='This is a \'single quote text\'...'
#A multiple line text string with triple double-quote
cadc="""Text line 1
Text line 2
Text line 3
"""
#Printing...
print(cads)
print(cadd)
print(cad)
print(cadc)